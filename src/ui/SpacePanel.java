package ui;

import javax.swing.*;
import java.awt.*;

/**
 * @author ypl
 * @date 2020/1/9
 */
public class SpacePanel extends JPanel {

    /**
     * 构造函数
     *
     * @param width 宽
     * @param height 高
     */
    public SpacePanel(int width, int height) {
        super();
        this.setPreferredSize(new Dimension(width, height));
    }

}
